//* Sirve para comparar datos entre variables, siempre va a indicarnos un valor booleano, true o false

let a = 10;

console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

//!Igualdad
console.log(a == 10);
console.log(a != 10);

//? Se usa el === o el  !== para verificar si es igual tanto su valor como su tipo.
console.log(a === '10');
console.log(a !== '10');

